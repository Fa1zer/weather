import Foundation

enum TimeFormat: String {
    
    case twentyFourTime = "HH:mm"
    case twelveTime = "h:mm a"
    case twentyFourWeekday = "HH:mm, E d MMMM"
    case twelveWeekday = "h:mm a, E d MMMM"
    case weekday = "dd/MM"
    
}
